package com.core.opperators;

public class RelationalOperators {

	public static void main(String[] args) {
		System.out.println("Relational Operators");
		System.out.println("\nComparison Operators\n\t( > >= < <=)");
		System.out.println("\t10 < 20: " + (10 < 20));
		System.out.println("\t10 <= 10: " + (10 < 10));
		System.out.println("\t10 > 20: " + (10 > 20));
		System.out.println("\t10 >= 10: " + (10 >= 10));
		System.out.println("\t'a' < 10: " + ('a' < 10));
		System.out.println("\t'a' > 'A': " + ('a' > 'A'));
		System.out.println("\t'a' > 900: " + ('a' > 900));
		System.out.println("\tchar set value 'a': " + (int) 'a');
		System.out.println("\tchar set value 'A': " + (int) 'A');

	}

}
